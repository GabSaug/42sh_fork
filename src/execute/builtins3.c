static void change_setenv(char *argv[], size_t i)
{
  size_t j = 0;
  for (j = 0; argv[i][j] && argv[i][j] != '='; j++)
    continue;
  if (argv[i][j])
  {
    argv[i][j] = '\0';
    setenv(argv[i], argv[i] + j + 1, 1);
  }
  else
  {
    char *env = getenv(argv[i]);
    if (env)
      return;
    else
      setenv(argv[i], "", 1);
  }
}

